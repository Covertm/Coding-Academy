var sectionCenter = document.querySelector(".section-center");
var btnContainer = document.querySelector(".btn-container");
var btnContainer2 = document.querySelector(".btn-container2");
var btnContainer3 = document.querySelector(".btn-container3");
// display all items when page loads
window.addEventListener("DOMContentLoaded", function () {
    displayMenuButtons();
    diplayMenuItems(menu);
    displayPrices(menu);
});
function diplayMenuItems(menuItems) {
    var displayMenu = menuItems.map(function (item) {
        // console.log(item);
        return "<article class=\"menu-item\">\n          <img src=" + item.img + " alt=" + item.title + " class=\"photo\" />\n          <div class=\"item-info\">\n            <header>\n              <h4>" + item.title + "</h4>\n              <h4 class=\"price\">$" + item.price + "</h4>\n              <div class=\"btn-container3\">\n                <p> " + getButton(item.id) + "</p>\n              </div>\n            </header>\n            <p class=\"item-text\">\n              " + item.desc + "\n            </p> \n          </div>\n        </article>";
    });
    displayMenu = displayMenu.join("");
    sectionCenter.innerHTML = displayMenu;
}
function displayMenuButtons() {
    var categories = menu.reduce(function (values, item) {
        if (!values.includes(item.category)) {
            values.push(item.category);
        }
        return values;
    }, ["all"]);
    var categoryBtns = categories
        .map(function (category) {
        return "<button type=\"button\" class=\"filter-btn\" data-id=" + category + ">\n          " + category + "\n        </button>";
    })
        .join("");
    btnContainer.innerHTML = categoryBtns;
    var filterBtns = btnContainer.querySelectorAll(".filter-btn");
    console.log(filterBtns);
    filterBtns.forEach(function (btn) {
        btn.addEventListener("click", function (e) {
            // console.log(e.currentTarget.dataset);
            var category = e.currentTarget.dataset.id;
            var menuCategory = menu.filter(function (menuItem) {
                // console.log(menuItem.category);
                if (menuItem.category === category) {
                    return menuItem;
                }
            });
            if (category === "all") {
                diplayMenuItems(menu);
            }
            else {
                diplayMenuItems(menuCategory);
            }
        });
    });
}
function displayPrices(menu) {
    var ItemPrice = menu.reduce(function (values, item) {
        if (!values.includes(item.price)) {
            values.push(item.price);
            values.sort();
        }
        return values;
    }, ["0.00"]);
    var ItemPriceBtns = ItemPrice
        .map(function (price) {
        return "<button type=\"button\" class=\"filter-btn2\" data-id=" + price + "> $" + price + "\n        </button>";
    })
        .join("");
    btnContainer2.innerHTML = ItemPriceBtns;
    var filterBtn2 = btnContainer2.querySelectorAll(".filter-btn2");
    console.log(filterBtn2);
    filterBtn2.forEach(function (btn) {
        btn.addEventListener("click", function (e) {
            // console.log(e.currentTarget.dataset);
            var price = e.currentTarget.dataset.id;
            var menuPrice = menu.filter(function (menuItem) {
                // console.log(menuItem.category);
                if (parseInt(menuItem.price) == parseInt(price)) {
                    return menuItem;
                }
            });
            if (price === "0.00") {
                diplayMenuItems(menu);
            }
            else {
                diplayMenuItems(menuPrice);
            }
        });
    });
}
